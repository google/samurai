from functools import wraps
from time import time


def timing(f):
    @wraps(f)
    def wrap(*args, **kw):
        ts = time()
        result = f(*args, **kw)
        te = time()
        print("\n\tfunc:%r took: %2.4f sec\n" % (f.__name__, te - ts))
        return result

    return wrap
